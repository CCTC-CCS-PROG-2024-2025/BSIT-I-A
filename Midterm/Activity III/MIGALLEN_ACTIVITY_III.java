public class MIGALLEN_ACTIVITY_III {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter number");
        int second = sc.nextInt();

        while (second >-1){
            System.out.println(second);
            second--;
        }
    }
}
    
