class CAMINGAO_ACTIVITY_IV {
    public static void main(String[] args) {   
        int a = 7;
        int b = 2;
        int c = 3;
        
        int sum  = a + b + c;
        int diff = a - b - c;
        int prod = a * b * c;
        int quot = a / b / c;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(prod);
        System.out.println(quot);
    }
}
