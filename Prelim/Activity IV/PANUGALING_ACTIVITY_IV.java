public class PANUGALING_ACTIVITY_IV {
    public static void main(String[] args) {
        // Create 3 Variables
        int age1 = 20;
        int age2 = 10;
        int age3 = 5;

        // Performing simple equations
        int sum = age1 + age2 + age3;
        int difference = age1 - age2 - age3;
        int product  = age1 * age2 * age3;
        int quotient = age1 / age2 / age3;

        // Printing Results
        System.out.println(sum);
        System.out.println(difference);
        System.out.println(product);
        System.out.println(quotient);
    } 
}