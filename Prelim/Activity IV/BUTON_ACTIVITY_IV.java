class BUTON_ACTIVITY_IV {
    public static void main (String[] args) {
        int a = 5;
        int f = 3;
        int d = 2;

        int sum = a + f + d;
        int diff = a - f - d;
        int prod = a * f * d;
        int quot = a / f / d;
        
        System.out.println(sum);
        System.out.println(diff);
        System.out.println(prod);
        System.out.println(quot);
    }              
}